    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

    String idText = jTextField2.getText();
    String status = jComboBox1.getSelectedItem().toString();

    if (idText.equals("") || !idText.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Enter valid Movie ID");
        return;
    }

    int movieId = Integer.parseInt(idText);

    boolean updated = WatchlistManager.updateStatus(movieId, status);

    if (updated) {
        JOptionPane.showMessageDialog(this, "Status updated");
        loadWatchlistTable();
        updateSummaryLabels();
    } else {
        JOptionPane.showMessageDialog(this, "Movie not found in watchlist");
    }


    }//GEN-LAST:event_jButton4ActionPerformed

