    private void loadWatchlistTable() {

    DefaultTableModel model =
        (DefaultTableModel) watchlistTable.getModel();

    model.setRowCount(0);

    ArrayList<Movie> list = WatchlistManager.getWatchlist();

    for (int i = 0; i < list.size(); i++) {
        Movie m = list.get(i);
        model.addRow(new Object[]{
            m.getID(),
            m.getName(),
            m.getGenre(),
            m.getYear(),
            m.getRating(),
            m.getWatchStatus()
        });
    }
}
    
    private void updateSummaryLabels() {

    int total = 0;
    int watching = 0;
    int completed = 0;
    int notWatched = 0;

    ArrayList<Movie> list = WatchlistManager.getWatchlist();

    for (int i = 0; i < list.size(); i++) {
        Movie m = list.get(i);
        total++;

        if (m.getWatchStatus().equals("Watching")) {
            watching++;
        } 
        else if (m.getWatchStatus().equals("Completed")) {
            completed++;
        } 
        else {
            notWatched++;
        }
    }

    jLabel3.setText(String.valueOf(total));
    jLabel4.setText(String.valueOf(watching));
    jLabel15.setText(String.valueOf(completed));
    jLabel16.setText(String.valueOf(notWatched));
}



