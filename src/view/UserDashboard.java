    DefaultTableModel model =
        (DefaultTableModel) movieTable.getModel();

    model.setRowCount(0);

    for (int i = 0; i < list.size(); i++) {
        Movie m = list.get(i);
        model.addRow(new Object[]{
            m.getID(),
            m.getName(),
            m.getGenre(),
            m.getYear(),
            m.getRating()
        });
    }


