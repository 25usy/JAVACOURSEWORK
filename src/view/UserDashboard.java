    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
    String keyword = txtSearch.getText();
    String criteria = searchByBox.getSelectedItem().toString();

    if (keyword.equals("")) {
        JOptionPane.showMessageDialog(this, "Please enter a search value");
        return;
    }

    DefaultTableModel model =
        (DefaultTableModel) movieTable.getModel();
    model.setRowCount(0);

    // Linear Search for Movie Name
    if (criteria.equals("Movie Name")) {

        ArrayList<Movie> results =
            controller.searchMovie(keyword, criteria);

        for (int i = 0; i < results.size(); i++) {
            Movie m = results.get(i);
            model.addRow(new Object[]{
                m.getID(),
                m.getName(),
                m.getGenre(),
                m.getYear(),
                m.getRating()
            });
        }

        if (results.size() == 0) {
            JOptionPane.showMessageDialog(this, "No matching movies found");
        }
    }

    // Binary Search for Release Year
    else if (criteria.equals("Release Year")) {

        if (!keyword.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid year");
            return;
        }

        int year = Integer.parseInt(keyword);
        Movie result = controller.binarySearchByYear(year);

        if (result != null) {
            model.addRow(new Object[]{
                result.getID(),
                result.getName(),
                result.getGenre(),
                result.getYear(),
                result.getRating()
            });
        } else {
            JOptionPane.showMessageDialog(this, "No movie found for this year");
        }
    }


    }//GEN-LAST:event_btnSearchActionPerformed

