    public void loadMovieTable() {
    DefaultTableModel model = (DefaultTableModel) movieTable.getModel();
    model.setRowCount(0); // clear table

    for (Movie m : controller.readMovie()) {
        model.addRow(new Object[]{
            m.getID(),
            m.getName(),
            m.getGenre(),
            m.getYear(),
            m.getRating()
        });
    }
  }
    
    public void loadRecentMovies() {

    DefaultTableModel model =
        (DefaultTableModel) recentTable.getModel();

    model.setRowCount(0);

    Queue<Movie> recentMovies = controller.getRecentMovies();

    // FIFO: iterate from front to rear
    for (Movie m : recentMovies) {
        model.addRow(new Object[]{
            m.getName()
        });
    }
  }



  


